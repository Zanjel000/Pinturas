    private void getTable(){
        String colums[] = { "ID", "Nombre", "Codigo", "cantidad", "color", "marca", "textura"};
        DefaultTableModel models = new DefaultTableModel(colums, 0);
        Object [] obj = new Object[7];
        List ls;
        
         try {
            ls = ctrPotes.findPotesEntities();
            for (int i = 0; i <ls.size(); i++) {
                pot = (Potes) ls.get(i);
                obj[0] = pot.getPotId();
                obj[1] = pot.getNombre();
                obj[2] = pot.getCodigo();
                obj[3] = pot.getCantidad();
                obj[4] = pot.getColor();
                obj[5] = pot.getMarca();
                obj[6] = pot.getTextura();
                
                models.addRow(obj);
            }
                this.TablePintu.setModel(models);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Error!!" + e.getMessage());
        }
    }

