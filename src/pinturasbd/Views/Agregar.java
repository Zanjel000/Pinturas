    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        if (!txtNombre.getText().equals("")
                && !txtCantidad.getText().equals("")
                && !txtCodigo.getText().equals("")
                && !txtColor.getText().equals("")
                && !txtMarca.getText().equals("")
                && !txtTextura.getText().equals("")){

            //Definir EntityManagerFactory
            EntityManagerFactory emf = new Persistence().createEntityManagerFactory("PinturasBDPU");
            //Definir la instancia del controlador
            PotesJpaController controller = new PotesJpaController(emf);

            //Obtener los valores de todos los campos
            String Nombre = txtNombre.getText();
            int Cantidad = Integer.parseInt(txtCantidad.getText());
            int codigo = Integer.parseInt( txtCodigo.getText());
            String Color = txtColor.getText();
            String marca = txtMarca.getText();
            String textura = txtTextura.getText();
            
           

            //Instanciar una universidad y darle todos los valores segun los obtenidos arriba
            Potes pot = new Potes();
            pot.setNombre(Nombre);
            pot.setCantidad(Cantidad);
            pot.setCodigo(codigo);
            pot.setColor(Color);
            pot.setMarca(marca);
            pot.setTextura(textura);
            //Definir un try catch
            try {
                //Accedemos al metodo crear del controlador y le pasamos la universidad creada arriba
                controller.create(pot);
                getTable();
                //Si sale bien mostramos
                JOptionPane.showMessageDialog(null, " SU PRODUCTO FUE AGREGADO ");
            } catch (Exception e) {
                //Si sale mal mostramos
                JOptionPane.showMessageDialog(null, "Hubo error, intentelo nuevamente");
            }
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

